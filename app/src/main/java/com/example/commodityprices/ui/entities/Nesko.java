package com.example.commodityprices.ui.entities;

public class Nesko {

    private String name;
    private String description;

    public Nesko(String name, String description) {
        this.name = name;
        this.description = description;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

}
